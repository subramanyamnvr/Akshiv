const defaultLanguage = "en";
const storedLanguage = localStorage.getItem("preferredLanguage");
const initialLanguage = translations[storedLanguage] ? storedLanguage : defaultLanguage;

const header = document.querySelector(".site-header");
const menuToggle = document.querySelector(".menu-toggle");
const siteNav = document.querySelector(".site-nav");
const phoneTarget = document.querySelector("[data-contact-phone]");
const emailTarget = document.querySelector("[data-contact-email]");
const whatsappLink = document.querySelector("[data-whatsapp-link]");
const callLink = document.querySelector("[data-call-link]");
const yearTarget = document.querySelector("[data-current-year]");
const revealTargets = document.querySelectorAll(".reveal");
const languageSelect = document.querySelector("#language-select");
const metaDescription = document.querySelector('meta[name="description"]');
const prefersReducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)");

function updateHeaderState() {
  if (!header) {
    return;
  }

  header.classList.toggle("is-scrolled", window.scrollY > 10);
}

function closeMenu() {
  if (!menuToggle || !siteNav) {
    return;
  }

  menuToggle.setAttribute("aria-expanded", "false");
  siteNav.classList.remove("is-open");
}

function translatePage(language) {
  const activeLanguage = translations[language] ? language : defaultLanguage;
  const dictionary = translations[activeLanguage];

  document.documentElement.lang = activeLanguage;
  document.title = dictionary["meta.title"];

  if (metaDescription) {
    metaDescription.content = dictionary["meta.description"];
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  if (languageSelect) {
    languageSelect.value = activeLanguage;
  }

  localStorage.setItem("preferredLanguage", activeLanguage);
  applyContactConfig(activeLanguage);
}

function applyContactConfig(language) {
  const dictionary = translations[language] || translations[defaultLanguage];
  const phoneDisplay = contactConfig.phoneDisplay || dictionary["contact.phonePlaceholder"];
  const emailDisplay = contactConfig.email || dictionary["contact.emailPlaceholder"];

  if (phoneTarget) {
    phoneTarget.textContent = phoneDisplay;
  }

  if (emailTarget) {
    emailTarget.textContent = emailDisplay;
  }

  if (yearTarget) {
    yearTarget.textContent = new Date().getFullYear();
  }

  if (contactConfig.phoneRaw) {
    if (whatsappLink) {
      whatsappLink.href = `https://wa.me/${contactConfig.phoneRaw}`;
      whatsappLink.target = "_blank";
      whatsappLink.rel = "noreferrer";
    }

    if (callLink) {
      callLink.href = `tel:+${contactConfig.phoneRaw}`;
    }
  }
}

function setupRevealAnimations() {
  if (!revealTargets.length) {
    return;
  }

  if (prefersReducedMotion.matches || !("IntersectionObserver" in window)) {
    revealTargets.forEach((target) => target.classList.add("is-visible"));
    return;
  }

  const revealObserver = new IntersectionObserver(
    (entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -40px 0px",
    }
  );

  revealTargets.forEach((target) => revealObserver.observe(target));
}

updateHeaderState();
setupRevealAnimations();
translatePage(initialLanguage);

window.addEventListener("scroll", updateHeaderState, { passive: true });

if (languageSelect) {
  languageSelect.addEventListener("change", (event) => {
    translatePage(event.target.value);
  });
}

if (menuToggle && siteNav) {
  menuToggle.addEventListener("click", () => {
    const isOpen = siteNav.classList.toggle("is-open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  siteNav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMenu);
  });
}
